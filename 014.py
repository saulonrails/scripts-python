a = int(input('Digite um valor: '))
b = int(input('Digite outro valor: '))
s = a + b
su = a - b
m = a * b
d = a / b
di = a // b
e = a ** b
print('A soma é {}, a subtração é {}, o produto é {}.'.format(s, su, m))
print('A divisão é {:.2f}, a divisão inteira é {}, a potência é {}.'.format(d, di, e))
